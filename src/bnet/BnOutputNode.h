//////////////////////////////////////////////////////////////////////
/// @class BnOutputNode BnNodeImpl.h "BnNodeImpl.h"
/// @brief 外部出力ノードを表すクラス
//////////////////////////////////////////////////////////////////////
class BnOutputNode :
  public BnNodeImpl
{
public:
  //////////////////////////////////////////////////////////////////////
  // コンストラクタ/デストラクタ
  //////////////////////////////////////////////////////////////////////

  /// @brief コンストラクタ
  /// @param[in] id ID 番号
  /// @param[in] name ノード名
  /// @param[in] output_id 出力番号
  BnOutputNode(ymuint id,
	       const string& name,
	       ymuint output_id);

  /// @brief デストラクタ
  virtual
  ~BnOutputNode();


public:
  //////////////////////////////////////////////////////////////////////
  // 全タイプ共通の外部インターフェイス
  //////////////////////////////////////////////////////////////////////

  /// @brief タイプを返す．
  virtual
  BnNodeType
  type() const;

  /// @brief 外部出力ノードの時 true を返す．
  virtual
  bool
  is_output() const;


public:
  //////////////////////////////////////////////////////////////////////
  // 外部出力ノードの外部インターフェイス
  //////////////////////////////////////////////////////////////////////

  /// @brief 出力番号を返す．
  ///
  /// is_output() == false の時の動作は不定<br>
  /// node = BnNetwork::output(id) の時，node->output_id() = id となる．
  virtual
  ymuint
  output_id() const;

  /// @brief 入力のノード番号を返す．
  ///
  /// is_output() == false の時の動作は不定
  virtual
  ymuint
  fanin() const;


public:
  //////////////////////////////////////////////////////////////////////
  // 設定用のインターフェイス
  //////////////////////////////////////////////////////////////////////

  /// @brief ファンインを設定する．
  /// @param[in] ipos 入力位置
  /// @param[in] fanin_id ファンインのノード番号
  virtual
  void
  set_fanin(ymuint ipos,
	    ymuint fanin_id);


private:
  //////////////////////////////////////////////////////////////////////
  // データメンバ
  //////////////////////////////////////////////////////////////////////

  // 出力番号
  ymuint mOutputId;

  // ファンインのノード番号
  ymuint mFanin;

};


//////////////////////////////////////////////////////////////////////
/// @class BnPortOutput BnPortOutput.h "BnPortOutput.h"
/// @brief 外部出力端子を表すクラス
//////////////////////////////////////////////////////////////////////
class BnPortOutput :
  public BnOutputNode
{
public:

  /// @brief コンストラクタ
  /// @param[in] id ID 番号
  /// @param[in] name ノード名
  /// @param[in] output_id 出力番号
  /// @param[in] port_id ポート番号
  /// @param[in] port_bit ポート中のビット位置
  BnPortOutput(ymuint id,
	       const string& name,
	       ymuint output_id,
	       ymuint port_id,
	       ymuint port_bit);

  /// @brief デストラクタ
  virtual
  ~BnPortOutput();


public:
  //////////////////////////////////////////////////////////////////////
  // 出力ノードの外部インターフェイス
  //////////////////////////////////////////////////////////////////////

  /// @brief 外部出力端子の時に true を返す．
  virtual
  bool
  is_port_output() const;


public:
  //////////////////////////////////////////////////////////////////////
  // 入力タイプ/出力タイプに共通なインターフェイス
  //////////////////////////////////////////////////////////////////////

  /// @brief 接続しているポート番号を返す．
  ///
  /// is_port_input() == true || is_port_output() == true の時のみ意味を持つ．
  virtual
  ymuint
  port_id() const;

  /// @brief 接続しているポート中のビット番号を返す．
  ///
  /// is_port_input() || is_port_output() の時のみ意味を持つ．
  virtual
  ymuint
  port_bit() const;


private:
  //////////////////////////////////////////////////////////////////////
  // データメンバ
  //////////////////////////////////////////////////////////////////////

  // ポート番号
  ymuint mPortId;

  // ポート中のビット位置
  ymuint mPortBit;

};


